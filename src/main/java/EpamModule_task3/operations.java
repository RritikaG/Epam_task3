package EpamModule_task3;

public class operations {
    public float Add(int x, int y)
    {
        return x+y;
    }
    public float Subtract(int x, int y)
    {
        return x-y;
    }
    public float Multiply(int x, int y)
    {
        return x*y;
    }
    public float Divide(int x, int y)
    {
        return x/y;
    }

}
